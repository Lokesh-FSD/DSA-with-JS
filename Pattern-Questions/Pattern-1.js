// print a pattern of start for (4 x 4) star pattern

// ****
// ****
// ****
// ****

let n = 4;

for (let i = 1; i <= n; i++) {
  let str = "";
  for (let j = 1; j <= n; j++) {
    str += "*";
  }
  console.log(str);
}

